function setGraphPlotAttributes()
fontname = 'Helvetica';
set(groot,'defaultaxesfontname',fontname);
set(groot,'defaulttextfontname',fontname);
set(groot,'defaultAxesXGrid','on'); set(groot,'defaultAxesXminorGrid','on')
set(groot,'defaultAxesYGrid','on'); set(groot,'defaultAxesYminorGrid','on')
set(groot,'defaultAxesZGrid','on'); set(groot,'defaultAxesZminorGrid','on')
% set(0,'defaultAxesXlabel','x');
% set(0,'defaultAxesYlabel','y');
end